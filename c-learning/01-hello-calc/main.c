/**
 * 项目 1：你好计算器
 * =====================
 * 学习目标：
 *   - printf() 输出文字到屏幕
 *   - scanf()  从键盘读取输入
 *   - 变量类型：int（整数）、float（小数）、char（字符）
 *   - 基本运算符：+ - * /
 *   - 格式化输出：%.2f 表示保留2位小数
 *
 * 运行方法：
 *   gcc main.c -o main.exe
 *   main.exe
 */

#include <stdio.h>  // 标准输入输出库，提供 printf 和 scanf

int main()
{
    // ===== 第1部分：打个招呼 =====
    printf("========================================\n");
    printf("      欢迎来到 C 语言计算器！\n");
    printf("========================================\n\n");

    // ===== 第2部分：声明变量 =====
    // 变量就像"贴标签的盒子"，用来存放数据
    float num1, num2;  // 两个 float 类型的变量，存放小数
    char op;           // 一个 char 类型的变量，存放运算符

    // ===== 第3部分：获取输入 =====
    // scanf 从键盘读取用户输入
    // %f 表示读取一个浮点数（float）
    // &num1 表示"num1 这个盒子的地址"，scanf 会把读到的数据放进这个地址
    printf("请输入第一个数字：");
    scanf("%f", &num1);  // 读取第1个数字

    printf("请输入运算符 (+, -, *, /)：");
    scanf(" %c", &op);   // 读取运算符
    // 注意 %c 前面有一个空格！这是为了跳过上一次输入留下的换行符

    printf("请输入第二个数字：");
    scanf("%f", &num2);  // 读取第2个数字

    // ===== 第4部分：计算并输出结果 =====
    printf("\n------------------\n");
    printf("计算结果：\n");

    // if-else if-else 是分支判断语句
    // 根据运算符的不同，执行不同的计算
    if (op == '+')
    {
        // %.2f 表示输出 float，保留2位小数
        printf("%.2f + %.2f = %.2f\n", num1, num2, num1 + num2);
    }
    else if (op == '-')
    {
        printf("%.2f - %.2f = %.2f\n", num1, num2, num1 - num2);
    }
    else if (op == '*')
    {
        printf("%.2f * %.2f = %.2f\n", num1, num2, num1 * num2);
    }
    else if (op == '/')
    {
        // 除法需要特殊处理：除数不能为 0
        if (num2 != 0)
        {
            printf("%.2f / %.2f = %.2f\n", num1, num2, num1 / num2);
        }
        else
        {
            printf("错误：除数不能为 0！\n");
        }
    }
    else
    {
        // 用户输入了不认识的运算符
        printf("错误：不支持的运算符 '%c'\n", op);
    }

    printf("------------------\n");
    printf("\n感谢使用！按回车键退出...");
    getchar();  // 消耗之前残留的换行符
    getchar();  // 等待用户按回车

    return 0;  // 返回 0 表示程序正常结束
}
